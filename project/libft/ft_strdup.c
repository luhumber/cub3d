#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*dest;
	int		count;

	i = -1;
	count = ft_strlen(s) + 1;
	dest = malloc(count * sizeof(char));
	if (!dest)
		return (NULL);
	while (s[++i])
		dest[i] = s[i];
	dest[i] = '\0';
	return (dest);
}
